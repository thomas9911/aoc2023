import aoc2023


def test_day03a_parse_tokens1():
    schema = """467
.*.
.9$"""

    schematic = aoc2023.day03a_parse_tokens(schema)
    assert {
        (0, 0): "4",
        (1, 0): "6",
        (2, 0): "7",
        (1, 1): "*",
        (1, 2): "9",
        (2, 2): "*",
    } == schematic.tokens


